#En Python no es necesario declarar el tipo de variable
#El tipo de variable se asigna automáticamente al momento de asignar un valor
#Los tipos de variables más comunes son:
entero = 10            #int
flotante = 10.5        #float
cadena = "Hola"        #str
booleano = True        #bool
lista = [1, 2, 3]      #list
tupla = (1, 2, 3)      #tuple
conjunto = {1, 2, 3}   #set

#¿Cómo saber el tipo de una variable?
#Se usa la función type()

print(type(entero))     #<class 'int'>
print(type(flotante))   #<class 'float'>

#¿Qué hacemos con las variables?
#podemos imprimirlas

print(entero)
print(flotante)
print(cadena)
print(booleano)
print(lista)
print(tupla)
print(conjunto)