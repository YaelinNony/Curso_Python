#Tipos de operaciones en python
#Aritméticos
a=10
b=3

print("Suma: ", a+b)
print("Resta: ", a-b)
print("Multiplicación: ", a*b)
print("División: ", a/b)
print("División entera: ", a//b)
print("Módulo: ", a%b) #¿Cuánto sobra en una división?
print("Exponente: ", a**b)

x = int(input("Dame un número: "))
y= int(input("Dame otro número: "))

print(f"La suma de {x} + {y} es: {x+y}")
print(f"La resta de {x} - {y} es: {x-y}")
print(f"La multiplicación de {x} * {y} es: {x*y}")
print(f"La división de {x} / {y} es: {x/y}")
print(f"La división entera de {x} // {y} es: {x//y}")
print(f"El módulo de {x} % {y} es: {x%y}")
print(f"El exponente de {x} ** {y} es: {x**y}")
