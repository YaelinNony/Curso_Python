#ejemplo practico para usar for y range
for numero in range(1, 11):
    print(f"El numero es: {numero}")
# Este script imprime los números del 1 al 10 utilizando un bucle for y la función range.
# La función range(1, 11) genera una secuencia de números desde 1 hasta 10 (el 11 no está incluido).
materiales = ["acero", "madera", "plástico"]
for material in materiales:
    print(f"El material es: {material.capitalize()}")