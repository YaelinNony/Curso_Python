#progrmacion orientada a objetos
#! La Programación Orientada a Objetos (POO) es un estilo de programación (un paradigma) que organiza el código alrededor de "objetos", 
#!en lugar de funciones y lógica secuencial.

#? En POO existen diferentes terminos
#? CLASE: Piensa en una Clase como el plano de un coche. El plano define que un coche tendrá un color, un modelo y podrá arrancar y frenar.
#? OBJETO: Un Objeto es el coche real que construyes a partir de ese plano. Puedes construir muchos objetos (un Tesla rojo, un Ford azul) a partir de una sola clase.
#! la POO agrupa los datos (atributos) y los comportamientos (métodos) en una sola "caja" (objeto).

# Para que se utiliza?

#! El objetivo principal de la POO es manejar la complejidad y hacer que el código sea más fácil de mantener y reutilizar.

#? Modularidad: Tu programa se convierte en un conjunto de "cajas" (objetos) que interactúan entre sí, haciendo el código más fácil de organizar.
#? Reutilización: Puedes crear una clase Vehiculo una vez y luego reutilizarla para crear clases más específicas como Coche y Motocicleta (esto se llama Herencia).
#? Mantenibilidad: Si hay un error en un Coche, solo necesitas arreglar la clase Coche, sin que afecte al resto de tu programa.
#? Modelar el Mundo Real: Es una forma muy natural de representar cosas del mundo real (un Usuario, un Producto, un Sensor) en tu código.

